import random

class Util:

    def __init__(self):
        pass

    @staticmethod
    def max_row(ws):
        '''
            get the last row of the sheet
        '''
        row = ws.max_row
        while row > 0:
            cells = ws[row]
            if all([cell.value is None for cell in cells]):
                row -= 1
            else:
                break
        return row

    @staticmethod
    def get_col_from_name(ws, column_name):
        columns = []
        for col in ws.iter_cols(min_row=1, max_row=1, max_col=ws.max_column):
            for cell in col:
                if not cell.value:
                    continue
                columns.append(cell.value)

        return columns.index(column_name) + 1

